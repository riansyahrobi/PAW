                <h3>Fasilitas</h3>
                <p><strong>Jeep</strong></p>
                <p>Pengelola Jeep di taman Tebing Breksi mulai melakukan uji kelaikan untuk menyambut lonjakan wisatawan saat libur lebaran. Pengelolaan Jeep di taman tebing breksi sudah berbadan hukum yaitu koperasi. Nantinya ada lonjakan kenaikan jumlah kunjungan di taman tebing breksi, dari yang biasanya hanya sebagian saja yang beroperasi, nantinya diprediksi semuanya beroperasi, bahkan jika berkaca tahun lalu, satu chip bisa sampai 3 hingga 4 kali jalan 1 hari.</p>

                <h3>Deskripsi</h3>

                <p>Pesona keindahan wisata jogja dari bekas tambang ini yaitu berupa guratan – guratan tebing batu yang mempesona mengingatkan kita pada tempat wisata Garuda Wisnu Kencana Bali yang termasuk bekas tambang juga. Tak hanya tebingnya yang indah, akan tetapi kita juga bisa naik ke atas tebing lewat anak tangga dari batu yang sudah dibuat oleh warga masyarakat sekitar. Kita dapat menyaksikan keindahan Kota Jogja dari ketinggian di atas tebing ini. Selain itu juga pemandangan Gunung Merapi yang bisa tampak jelas bila cuaca sedang cerah. Terlebih apabila kita berkunjung pada saat malam hari, kita bisa menyaksikan indahnya kelip – kelip lampu yang tersebar di Kota Jogja.</p>

